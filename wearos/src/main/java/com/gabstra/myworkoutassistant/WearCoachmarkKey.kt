package com.gabstra.myworkoutassistant

/**
 * Keys for Wear OS coachmarks (exercise, rest, pages, sets, indicators, heart rate).
 */
enum class WearCoachmarkKey {
    // Exercise screen
    ExerciseTitle,
    ExercisePages,
    ExerciseButtons,

    // Rest screen
    RestTimer,
    RestExercisesPager,

    // Navigation / pages
    PageExercises,
    PagePlates,
    PageNotes,
    PageButtons,

    // Set screens
    SetTimer,
    SetReps,
    SetWeight,

    // Indicators and charts
    ExerciseIndicator,
    HeartRateChart,
}





